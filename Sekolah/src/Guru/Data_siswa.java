    private void tambahsiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahsiswaActionPerformed
        // TODO add your handling code here:

        try {
           
           PreparedStatement stmt = conn.prepareStatement("INSERT INTO siswa (nis, nama_siswa,  no_telp, alamat, kode_jurusan) values (?,?,?,?,?)");
            stmt.setString(1, NIS.getText());
            stmt.setString(2, namasiswa.getText());
            stmt.setString(3,telpsiswa.getText());
            stmt.setString(4, almt.getText());
            stmt.setString(5, (String) kdjurusan.getSelectedItem());
       
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            tabelsiswa();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Menambahkan" + e.getMessage());

        }
    }//GEN-LAST:event_tambahsiswaActionPerformed

