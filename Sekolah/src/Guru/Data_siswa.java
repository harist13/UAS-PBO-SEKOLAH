  void searchsiswa (){
        DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("nis");
        tbl.addColumn("nama siswa");
        tbl.addColumn("alamat");
        tbl.addColumn("no telp");
        tbl.addColumn("tingkat");
         tbl.addColumn("jurusan");
        
        try {
            String sql = "SELECT * FROM siswa INNER JOIN jurusan ON siswa.kode_jurusan=jurusan.kode_jurusan WHERE nama_siswa LIKE '%" + searchsiswa.getText() +  "%'";
            Connection con = (Connection) KoneksiDB.getKoneksi();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                tbl.addRow(new Object[] {
                    rs.getString("NIS"),
                    rs.getString("nama_siswa"),
                    rs.getString("alamat"),
                    rs.getString("no_telp"),
                     rs.getString("tingkat"),
                     rs.getString("jurusan"),

                });
                tbsiswa.setModel(tbl);
            }
            
        } catch (Exception e) {
            System.out.println(e);
        }
    }

